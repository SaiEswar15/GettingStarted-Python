
number = ""

number = 12.895
print(type(number))

#number to string
number = str(number)
print(type(number))

#string to float
number = float(number)
print(type(number))

#float to int
number = int(number)
print(type(number))
print(number)

#int to bool
number = bool(number)
print(type(number))
print(number)