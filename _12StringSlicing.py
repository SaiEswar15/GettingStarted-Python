name = "eswar"

#get the first index
print(name[0])

#get the last index
print(name[len(name)-1])

#find sub string of the string
print("war" in name)

#slicing from one index to other index
print(name[1 : 3])

#slicing the white spaces
name1 = "     eswra    "
print(name1.strip())

#slicing going till last 
print(name[2:])
print(name[:4])
